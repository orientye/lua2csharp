// int
int n1 = 2 + 3;            // n1 is 5
int n2 = n1 - 1;           // n2 is 4
int n3 = n2 * 2;           // n3 is 8
float n4 = 16 + n3 / 5f;   // n4 is 17.6

// float
float f1 = 0.4f;           // f1 is a float with value 0.4
float f2 = 4.57e-3f;       // f2 is a float with value 0.00457
float f3 = 0.3e12f;        // f3 is a float with value 300000000000
float f4 = 5e+20f;         // f4 is a float with value 500000000000000000000

// bool
bool b1 = false;           // b1 is a boolean set to false
bool b2 = b1;              // b2 is also false (same as b1)

// string
string s1 = "i am a string"; // s1 is a string
string s2 = s1 + " append";   // s2 is now "i am a string append"